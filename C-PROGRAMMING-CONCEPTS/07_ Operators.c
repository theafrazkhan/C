#include <stdio.h>

int main()
{
    int a, b;
    a = 2;
    b = 3;

    printf("a & b = %d\n", a & b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);

    return 0;
}
